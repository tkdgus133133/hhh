/**
 * datas/lib/data.ts → frontend/static/hungary-export-data.js
 * PRODUCTS ~ VERDICT_KO 블록만 반영합니다.
 */
const fs = require("fs");
const path = require("path");

const root = path.join(__dirname, "..");
const tsPath = path.join(root, "datas", "lib", "data.ts");
const outPath = path.join(root, "frontend", "static", "hungary-export-data.js");

const lines = fs.readFileSync(tsPath, "utf8").split(/\r?\n/);

const startIdx = lines.findIndex((l) => l.includes("export const PRODUCTS"));
const vkStart = lines.findIndex((l) => l.includes("export const VERDICT_KO"));
let endIdx = -1;
if (vkStart >= 0) {
  for (let i = vkStart + 1; i < lines.length; i++) {
    if (/^\};$/.test(lines[i].trim())) {
      endIdx = i;
      break;
    }
  }
}
if (startIdx < 0 || endIdx < 0) {
  console.error("Could not find PRODUCTS or VERDICT_KO block boundaries");
  process.exit(1);
}

let chunk = lines.slice(startIdx, endIdx + 1).join("\n");

chunk = chunk
  .replace(/\s+as const/g, "")
  .replace(/export const PRODUCTS = /, "PRODUCTS: ")
  .replace(/export const PRODUCT_DATA: Record<string, ProductEntry> = /, "PRODUCT_DATA: ")
  .replace(/export const COMPETITOR_DATA: Record<string, CompetitorEntry> = /, "COMPETITOR_DATA: ")
  .replace(/export const STATIC_DATA: Record<string, StaticEntry> = /, "STATIC_DATA: ")
  .replace(/export const VT: Record<string, string> = /, "VT: ")
  .replace(/export const VERDICT_KO: Record<string, string> = /, "VERDICT_KO: ");

/* 섹션 헤더 주석 제거 (치환 방해 방지) */
chunk = chunk
  .split("\n")
  .filter((ln) => !/^\s*\/\/ ──/.test(ln))
  .join("\n");

/* 객체 리터럴 속성 구분 쉼표 — TS 문장 종료 `];` `};` → 속성 구분자 */
chunk = chunk
  .replace(/\];\s*\n\s*PRODUCT_DATA:/, "],\n\n  PRODUCT_DATA:")
  .replace(/\};\s*\n\s*COMPETITOR_DATA:/, "},\n\n  COMPETITOR_DATA:")
  .replace(/\};\s*\n\s*STATIC_DATA:/, "},\n\n  STATIC_DATA:")
  .replace(/\};\s*\n\s*VT:/, "},\n\n  VT:")
  .replace(/\};\s*\n\s*VERDICT_KO:/, "},\n\n  VERDICT_KO:");

/* 마지막 VERDICT_KO 블록을 `};` → `}` (바깥 window 객체가 `};`로 닫힘) */
chunk = chunk.replace(/\};\s*$/, "}");

const out =
  "/* Auto-generated by scripts/build-hungary-export-data.cjs from datas/lib/data.ts */\n" +
  "'use strict';\n" +
  "window.HUNGARY_EXPORT = {\n  " +
  chunk.trim() +
  "\n};\n";

fs.writeFileSync(outPath, out, "utf8");
console.log("Wrote", outPath);
